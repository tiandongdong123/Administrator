package com.wf.bean.importSolr;

public class AdministratorRequest {
	
	private String AdministratorEmail;
	private String AdministratorPassword;
	private String AdministratorId;
	private String AdministratorOpenIP;

	public String getAdministratorEmail() {
		return AdministratorEmail;
	}

	public void setAdministratorEmail(String administratorEmail) {
		AdministratorEmail = administratorEmail;
	}

	public String getAdministratorPassword() {
		return AdministratorPassword;
	}

	public void setAdministratorPassword(String administratorPassword) {
		AdministratorPassword = administratorPassword;
	}

	public String getAdministratorId() {
		return AdministratorId;
	}

	public void setAdministratorId(String administratorId) {
		AdministratorId = administratorId;
	}

	public String getAdministratorOpenIP() {
		return AdministratorOpenIP;
	}

	public void setAdministratorOpenIP(String administratorOpenIP) {
		AdministratorOpenIP = administratorOpenIP;
	}

}
