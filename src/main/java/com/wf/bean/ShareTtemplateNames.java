package com.wf.bean;

public class ShareTtemplateNames {

	private Integer id;
	private String templet_name;
	private String templet_name_en;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getTemplet_name() {
		return templet_name;
	}

	public void setTemplet_name(String templet_name) {
		this.templet_name = templet_name;
	}

	public String getTemplet_name_en() {
		return templet_name_en;
	}

	public void setTemplet_name_en(String templet_name_en) {
		this.templet_name_en = templet_name_en;
	}

}
